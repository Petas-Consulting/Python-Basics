'''
Variables
#Variables are named containers for storing data values. Any piece of information that we wish to reference multiple
#times can be stored in a variable for later use.
Rules for Python variables:

    * Variables should start with letters or the underscore character
    * It should always start with lowercase alphabets
    * There should be no space in between the variable name
    * It cannot start with a number
    * It can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
    * Variable names are case-sensitive (hello, Hello and HELLO are three different variables)
'''
